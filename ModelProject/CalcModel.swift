import Foundation

class CalcModel{
    
    var number1 = Int()
    var number2 = Int()
    
    
   func plus(number1:Int,number2:Int)->Int{
          
          return number1 + number2
        
      
      }
      
      
      func mius(number1:Int,number2:Int)->Int{
          
          return number1 - number2
        
      
      }
      
      
      func kakeru(number1:Int,number2:Int)->Int{
          
          return number1 * number2
        
      
      }
      
      func waku(number1:Int,number2:Int)->Int{
          
          return number1 / number2
        
      
      }
       
    
    
}



